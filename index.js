function add(a, b) {
  return a + b
}

function subtract(b, a) {
  return b - a
}

function multiply(a, b) {
  return parseInt(a) * parseInt(b)
}

function divide(a, b) {
  return parseInt(a) / parseInt(b)
}

function inc(n) {
  return parseInt(n + 1)
}

function dec(n) {
  return parseInt(n - 1)
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
